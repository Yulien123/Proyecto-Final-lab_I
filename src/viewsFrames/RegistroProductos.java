    private void cargarProductos() {
        for (Producto item : listarP) {
            jcbCodProducto.addItem(item);
        }
    }

    private void vaciarCampos() {
        jcbCodProducto.setSelectedIndex(-1);
        jTNombre.setText("");
        jTDescripcion.setText("");
        jTPrecio.setText("");
        jTCantidad.setText("");
    }

